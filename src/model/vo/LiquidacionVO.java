package model.vo;

public class LiquidacionVO {

	private String numliq;
	private String tipo;
	private String mon;
	private String montous;
	private String montomn;

	public String getNumliq() {
		return numliq;
	}

	public void setNumliq(String numliq) {
		this.numliq = numliq;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getMon() {
		return mon;
	}

	public void setMon(String mon) {
		this.mon = mon;
	}

	public String getMontous() {
		return montous;
	}

	public void setMontous(String montous) {
		this.montous = montous;
	}

	public String getMontomn() {
		return montomn;
	}

	public void setMontomn(String montomn) {
		this.montomn = montomn;
	}

}
